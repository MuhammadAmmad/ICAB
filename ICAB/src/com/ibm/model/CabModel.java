package com.ibm.model;

public class CabModel {
private String cabname;
private String cabnumber;
private String cabtype;
private String nseats;
private String cabcolor;
private String status;
public String getCabname() {
	return cabname;
}
public void setCabname(String cabname) {
	this.cabname = cabname;
}
public String getCabnumber() {
	return cabnumber;
}
public void setCabnumber(String cabnumber) {
	this.cabnumber = cabnumber;
}
public String getCabtype() {
	return cabtype;
}
public void setCabtype(String cabtype) {
	this.cabtype = cabtype;
}
public String getNseats() {
	return nseats;
}
public void setNseats(String nseats) {
	this.nseats = nseats;
}
public String getCabcolor() {
	return cabcolor;
}
public void setCabcolor(String cabcolor) {
	this.cabcolor = cabcolor;
}
public String getStatus() {
	return status;
}
public void setStatus(String status) {
	this.status = status;
}
}
